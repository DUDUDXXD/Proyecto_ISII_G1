    private void buscarMedicamento(){
        String texto = textomedicamentos.getText();
        if(texto.trim().isEmpty()){
            rowSorter.setRowFilter(null);
        } else {
            rowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + texto, 0));
        }
    }
